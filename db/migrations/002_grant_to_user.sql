GRANT CONNECT ON DATABASE ${POSTGRES_DB} TO ${POSTGRES_USER};

GRANT USAGE ON SCHEMA public TO ${POSTGRES_USER};
GRANT CREATE ON SCHEMA public TO ${POSTGRES_USER};

